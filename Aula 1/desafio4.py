print("Este é um programa que calcula a média de um aluno com base em suas duas notas")
nota1 = float(input("Por favor, insira a primeira nota: "))
nota2 = float(input("Por favor, insira a segunda nota: "))
print(f"\nVocê digitou as notas {nota1} e {nota2}")
mediaNotaAluno = (nota1 + nota2) /2
print(f"A média desse aluno é {mediaNotaAluno}")
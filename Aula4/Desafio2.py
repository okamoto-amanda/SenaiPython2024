# Escreva um programa que leia dois números inteiros e
# compare- os, mostrando na tela uma mensagem:

print("Este é um programa que compara dois valores")
a = int(input("Digite o primeiro valor: "))
b = int(input("Digite o primeiro segundo: "))

if a > b :
    print("O primeiro valor é maior")
elif a < b :
    print("O segundo valor é maior")
else :
    print("São valores iguais")
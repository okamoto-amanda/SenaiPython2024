 
#Crie um algoritmo que leia um número e mostre o seu dobro, triplo e o seu quadrado
numeroBase = int(input("Por favor digite um número que te retornarei o dobro, o tripo e seu quadrado: "))
dobroNumero = numeroBase * 2 
triploNumero = numeroBase * 3 
quadradoDoNumero = numeroBase **2 

print(numeroBase, dobroNumero, triploNumero, quadradoDoNumero)
print(f"Você digitou o número {numeroBase} \n Seu dobro é {dobroNumero} \n Seu triplo é {triploNumero} \n {numeroBase}² = {quadradoDoNumero}") 
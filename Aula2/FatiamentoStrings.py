texto = "Curso de Python"
nome = "Amanda Okamoto"

#Capturando o sétimo caracter
print(texto[6])

#Capturando o oitavo caracter do nome
print(nome[7])

#Capturando uma seção de caracteres
print(texto[9:15])
print(nome[7:14])

#Capturando a partir de um caractere
print(texto[9:])

#Pulando caractere
print(texto[9:15:2])
print(texto[9::2])